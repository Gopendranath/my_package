    public boolean validateBook() {
        String bid = bkid.getText();
        String name = bkname.getText();
        String pub = publisher.getText();
        String aut = author.getText();
        String pr = price.getText();
        String yr = yrofpub.getText();
        String qu = quan.getText();
        String cl = callno.getText();
        
        if(bid.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter book id","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(name.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter book name","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
         
        if(pub.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter publisher","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
         
        if(aut.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter author","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(pr.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter price","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(yr.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter year of publishing","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(qu.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter quantity","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(cl.equals("")) {
            JOptionPane.showMessageDialog(this, "please enter call number","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        
        return true;
    }

