    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        if(validateBook() == true) {
        try{
            String query = "INSERT INTO `bookdetails`(`bookid`, `bname`, `publisher`, `author`, `price`, `yearofpub`, `quantity`, `call no.`) VALUES (?,?,?,?,?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost/libraryproject", "root", "");
            pst = con.prepareStatement(query);
            pst.setString(1,bkid.getText());
            pst.setString(2,bkname.getText());
            pst.setString(3,publisher.getText());
            pst.setString(4,author.getText());
            pst.setString(5,price.getText());            
            pst.setString(6,yrofpub.getText());
            pst.setString(7,quan.getText());
            pst.setString(8,callno.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "BOOK ADDED SUCCESSFULLY");
            AdminLogin1 adm = new AdminLogin1();
            adm.show();
            dispose();                       

        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);

        }
        }
    }//GEN-LAST:event_submitActionPerformed

